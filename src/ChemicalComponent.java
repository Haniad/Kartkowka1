
public class ChemicalComponent {

    private String symbol;
    private String licznosc;

    public ChemicalComponent(String symbol) {
        this.symbol = symbol;
    }

    public String getSymbol() {
        return symbol;
    }

    public String getLicznosc() {
        return licznosc;
    }

    public void setLicznosc(String licznosc) {
        this.licznosc = licznosc;
    }

}
